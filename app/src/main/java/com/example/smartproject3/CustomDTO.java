package com.example.smartproject3;

public class CustomDTO {
    int resID;
    int backImage;
    int foreImage;
    String title;

    public int getResID() {
        return resID;
    }

    public void setResID(int resID) {
        this.resID = resID;
    }

    public int getBackImage() {
        return backImage;
    }

    public void setBackImage(int backImage) {
        this.backImage = backImage;
    }

    public int getForeImage() {
        return foreImage;
    }

    public void setForeImage(int foreImage) {
        this.foreImage = foreImage;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
